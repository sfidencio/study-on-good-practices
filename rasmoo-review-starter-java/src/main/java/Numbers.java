public class Numbers {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;
        int soma = a + b;
        System.out.printf("%d", soma);

        System.out.println();

        float preco1 = 2.30f;
        float preco2 = 4.40f;

        System.out.printf("%2f%n", preco1 + preco2);
        System.out.printf("%6f%n", preco1 / preco2);

    }
}
